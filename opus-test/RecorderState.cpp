#include "stdafx.h"

#include "RecorderState.h"

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Constructor

RecorderState::RecorderState()
{
   reset();
}

void RecorderState::reset()
{
   mSX = cState_Stopped;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Return the state as a string.

const char* RecorderState::asString()
{
   switch (mSX)
   {
   case cState_Stopped:    return "Stopped  ";
   case cState_Recording:  return "Recording";
   case cState_Paused:     return "Paused   ";
   default: return "UNKNOWN";
   }
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Set state variables.

void RecorderState::setStopped()
{ 
   mSX = cState_Stopped;
}

void RecorderState::setRecording()
{
   mSX = cState_Recording;
}

void RecorderState::setPaused()
{
   mSX = cState_Paused;
}

//******************************************************************************
//******************************************************************************
//******************************************************************************
// Test state variables.

bool RecorderState::isStopped()
{
   return mSX == cState_Stopped;
}

bool RecorderState::isRecording()
{
   return mSX == cState_Recording;
}

bool RecorderState::isPaused()
{
   return mSX == cState_Paused;
}

